            <h3>Introduction</h3>
            <p>News & Views(N&V) is our weekly institutional newsletter, highlighting programs, publications, and other events at MEI. Scott will add the main body content to the newsletter, usually on Friday mornings. The rest of the newsletter, namely the Sidebar and “Scholars in the News” section, is filled in by the interns and should be ready before noon on Fridays to allow time for Scott and Kate to review it before sending out around 2pm.</p>
            <p>You can see a sample issue of N&V <a href="http://myemail.constantcontact.com/A-Look-at-Egypt-s-Short-and-Long-Term-Challenges---News---Views-.html?soid=1102919617474&aid=fKvkN5LLs7E" target="_blank">here</a>.</p>
          </section>
          <section>
            <h3>Sidebar Sections</h3>
            <ul>
              <li><strong>New Publications:</strong> Title, Author, and Link to articles published on MEI’s website since the last issue of N&V</li>
              <li><strong>Upcoming Events:</strong> Date, Time, Event Title, and Link to any upcoming events that have corresponding Event pages on the site.</li>
              <li><strong>MEJ Editor’s Blog:</strong> Linked Titles for three recent blog posts by our Middle East Journal editor, Michael Dunn.  We usually try to include two of his more research-heavy pieces, and one funny one.</li>
              <li><strong>MAP Highlights:</strong> Linked Titles to new publications on The Middle East-Asia Project page, if any new ones have been posted in the past week.</li>
              <li><strong>Middle East Journal:</strong> Linked Photo of the cover of the most recent MEJ. We only have to change this four times a year ;-)</li>
              <li><strong>Follow MEI and MEI Directory:</strong> shouldn’t have to touch these.</li>
            </ul>
          </section>
          <section>
            <h3>'Scholars in the News' Section</h3>
            <p>
              This is the original purpose of the News & Views newsletter--to draw attention to the weekly activities of our 40 Middle East scholars--and they do read this section closely to make sure their media hits were included, so be careful to add everything they send us!
            </p>
            <p>
              The challenge is how to make this section not totally boring.  Using the Media Tracking sheet, add sections for the relevant categories (usually the name of the country that is the primary subject of the media hits) and list the individual Scholar records.  If one scholar has spoken on multiple topics, choose whichever you think is best and go ahead and mention the other hits within the same paragraph.
            </p>
          </section>
