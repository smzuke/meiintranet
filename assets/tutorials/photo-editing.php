            <h3>Introduction</h3>
            <p>
              Most of the photos on our website come from Newscred, which licenses  high resolution images at a standard aspect ratio. These files are often 1-2 MB, and sometimes as high as 5 MB, larger than an average mp3 music file (or a Super Nintendo game!).
            </p>
            <p>
              The rotator (or carousel) at the top of MEI's homepage loads 5 or 6 photos every time someone goes to mei.edu, even though you only see one at a time. To keep the site running smoothly, we want to drastically reduce the file size of photos before we upload them to the site.
            </p>
            <p>
              This is a very brief tutorial on how to crop and resize images using GIMP and Pixlr, two free options for image editing.  Using these tools, it's quick and simple to reduce the filesize of large images sometimes by as much as 98% without any noticeable loss of quality when it appears on our site.
            </p>
